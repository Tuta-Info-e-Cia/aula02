print(10,20,30,sep="-",end="#")
print(100,200,300,sep="*",end="\n")
print(1000,2000,3000,sep="*",end="\n")
#"sep" = separador
# "end"= terminador
#quando o terminador é "#"" ele nao pula de linha na exibição do código escrito
# quando o terminador é "\n" ele pula automaticamente de linha ao imprimir os valores na tela.
print("André", "Simone", "Curso", sep='*', end='#')
